'''
This is the Card class defined for the BlackJack Game
'''

#***************************************************************************************************************************************
#--------------------------------------------------------Global Variables---------------------------------------------------------------
suits = ['H','D','S','C']
ranks = ['02','03','04','05','06','07','08','09','10','Jk','Qn','Kg','Ac']
val = {'02':2,'03':3,'04':4,'05':5,'06':6,'07':7,'08':8,'09':9,'10':10,'Jk':10,'Qn':10,'Kg':10,'Ac':11}
#****************************************************************************************************************************************
#--------------------------------------------------------   Card Class   ----------------------------------------------------------------
class Card():

	def __init__(self,suit,rank):
		self.suit = suit
		self.rank = rank
		self.value = val[rank]

	def __str__(self):
		return self.rank + ':' + self.suit

	def __len__(self):
		return self.value

